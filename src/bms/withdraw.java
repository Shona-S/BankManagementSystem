    private void withdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawActionPerformed
        // TODO add your handling code here:
        String amountText = amountTF.getText();
        try {
            double amount = Double.parseDouble(amountText);

            if (amount <= 0) {
                JOptionPane.showMessageDialog(null, "Enter a valid amount!");
                return;
            }

            DBconnection c = new DBconnection();

            // Get current balance
            String query = "SELECT balance FROM signup2 WHERE card_number = '" + card_number + "'";
            ResultSet rs = c.s.executeQuery(query);

            if (rs.next()) {
                double currentBalance = rs.getDouble("balance");

                if (amount > currentBalance) {
                    JOptionPane.showMessageDialog(null, "Insufficient balance!");
                    return;
                }

                // Update balance in DB
                String updateQuery = "UPDATE signup2 SET balance = balance - " + amount + " WHERE card_number = '" + card_number + "'";
                c.s.executeUpdate(updateQuery);

                // Insert transaction record
                String insertQuery = "INSERT INTO transactions (card_number, type, amount) VALUES ('" + card_number + "', 'withdraw', " + amount + ")";
                c.s.executeUpdate(insertQuery);

                JOptionPane.showMessageDialog(null, "Withdrawal successful! Remaining balance: â‚¹" + (currentBalance - amount));
            } else {
                JOptionPane.showMessageDialog(null, "Card number not found!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter a valid number.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error during withdrawal!");
        }
    }//GEN-LAST:event_withdrawActionPerformed

