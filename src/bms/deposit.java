    private void depositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositActionPerformed

        String amountText = amountTextField.getText();
        try {
            double amount = Double.parseDouble(amountText);

            if (amount <= 0) {
                JOptionPane.showMessageDialog(null, "Enter a valid amount!");
                return;
            }

            DBconnection c = new DBconnection();

            // Update balance in DB
            String updateBalanceQuery = "UPDATE signup2 SET balance = balance + " + amount + " WHERE card_number = '" + card_number + "'";
            c.s.executeUpdate(updateBalanceQuery);

            // Insert transaction record
            String insertTransactionQuery = "INSERT INTO transactions (card_number, type, amount) VALUES ('" + card_number + "', 'deposit', " + amount + ")";
            c.s.executeUpdate(insertTransactionQuery);

            // Fetch updated balance to show user
            String selectBalanceQuery = "SELECT balance FROM signup2 WHERE card_number = '" + card_number + "'";
            ResultSet rs = c.s.executeQuery(selectBalanceQuery);
            if (rs.next()) {
                double updatedBalance = rs.getDouble("balance");
                JOptionPane.showMessageDialog(null, "Amount deposited successfully! Current balance: â‚¹" + updatedBalance);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter a valid number.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error during deposit!");
        }
    }//GEN-LAST:event_depositActionPerformed

